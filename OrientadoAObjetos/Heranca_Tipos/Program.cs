Herança Simples:
A herança simples em C# ocorre quando uma classe é derivada de outra classe. A classe derivada herda os membros (campos, propriedades, métodos) da classe base. Isso facilita a reutilização de código e a extensão de funcionalidades.

Herança Múltipla (Interface):
Embora a herança múltipla de classes diretas não seja permitida em C#, é possível alcançar um efeito semelhante usando interfaces. As interfaces definem contratos que as classes podem implementar, permitindo a herança múltipla de comportamentos.

Herança Hierárquica:
Na herança hierárquica, várias classes derivam de uma única classe base. Isso cria uma estrutura de árvore, onde as classes mais específicas herdam características mais gerais da classe base. Isso facilita a organização e compreensão da hierarquia de classes.

Herança Multinível:
Uma classe pode herdar de outra que, por sua vez, herda de uma terceira classe, criando uma cadeia hierárquica.

Herança de Construtores:
A herança de construtores permite que uma classe derivada invoque o construtor da classe base. Isso é útil para inicializar membros da classe base antes de realizar inicializações específicas da classe derivada.

Herança com Métodos Virtuais e Override:
Os métodos virtuais em uma classe base podem ser substituídos por métodos na classe derivada usando a palavra-chave override. Isso possibilita a personalização do comportamento dos métodos na classe derivada, promovendo a flexibilidade e a polimorfia.
*/